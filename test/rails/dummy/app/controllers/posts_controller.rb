class PostsController < ApplicationController
  before_action :set_post, only: %i[ show edit update destroy ]

  CREATED = "Post was successfully created."
  UPDATED = "Post was successfully updated."
  DESTROYED = "Post was successfully destroyed."

  # GET /posts or /posts.json
  def index
    @posts = Post.all
  end

  # GET /posts/1 or /posts/1.json
  def show
  end

  # GET /posts/new
  def new
    @post = Post.new
  end

  # GET /posts/1/edit
  def edit
  end

  # POST /posts or /posts.json
  def create
    @post = Post.new(post_params)

    respond_to do |format|
      if @post.save
        format.html { redirect_to @post, notice: CREATED }
      else
        format.html { render :new, status: :unprocessable_entity }
      end

      # format.js
    end
  end

  # PATCH/PUT /posts/1 or /posts/1.json
  def update
    respond_to do |format|
      if @post.update(post_params)
        format.html { redirect_to @post, notice: UPDATED }
        format.json { render :show, status: :ok, location: @post }
      else
        format.html { render :edit, status: :unprocessable_entity }
        format.json { render json: @post.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /posts/1 or /posts/1.json
  def destroy
    # Give us time to show we're destroying.
    sleep(1)
    @post.destroy

    respond_to do |format|
      format.html { redirect_to posts_url, notice: DESTROYED }
    end
  end

  private

  # Use callbacks to share common setup or constraints between actions.
  def set_post
    @post = Post.find(params[:id])
  end

  # Only allow a list of trusted parameters through.
  def post_params
    params.require(:post).permit(:title, :body, :primary_image, secondary_images: [])
  end
end
